public class StayPositive {

    public static void main(String[] args) {

        System.out.println("Counting down...");

        int c = 11;
        int d = 0;

        while (c > d) {
            c--;
            System.out.println(c);

        }

        System.out.println("BLAST OFF!");

    }
}

