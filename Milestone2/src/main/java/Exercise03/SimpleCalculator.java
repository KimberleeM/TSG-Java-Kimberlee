public class SimpleCalculator {

    
    public float add(float  operands1, float  operands2){
        return operands1 + operands2;
    }
    
    public float multiply(float  operands1, float  operands2){
        return operands1 * operands2;
    }
    
    public float divide(float  operands1, float  operands2){
        return operands1 / operands2;
    }
    
    public float subtract(float  operands1, float  operands2){
        return operands1 - operands2;
    }

 
}  

